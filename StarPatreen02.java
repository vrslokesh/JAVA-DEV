public class StarPatreen02 {
    public static void main(String[] args) {
        System.out.println("======Star 01========");
        for(int i=0;i<=5;i++){
            for(int j=0;j<=i;j++){
                System.out.print("*");
            }
            System.out.println("");

        }
        System.out.println("======Star 02========");
        for(int k=0;k<=5;k++){
            for(int l=1;l<=5-k;l++){
                System.out.print("*");
            }
            System.out.println("");

        }
        System.out.println("======Star 03========");
        for(int i=0;i<=7;i++){
            for(int j=0;j <= 3;j++){
                System.out.print("*");
            }
            System.out.println("");

        }

    }
}
